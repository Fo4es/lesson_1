let day = prompt("Choose day");
switch (day){
       case "1":
        document.write('<div>Monday</div>');
        break;
       case "2":
        document.write('<div>Thusday</div>');
        break;
       case "3":
        document.write('<div>Wensday</div>');
        break;
        case "4":
        document.write('<div>Thursday</div>');
        break;
        case "5":
        document.write('<div>Friday</div>');
        break;
        case "6":
        document.write('<div>Saturday</div>');
        break;
        case "7":
        document.write('<div>Sunday</div>');
}

